package punto.venta.misclases;

public class InventaMovi {
    
    String idMovInv;
    String hora;
    String descripcion;
    String movimiento;
    String habia;
    String cantidad;
    String hay;
    String cajero;
    String departamento;
    String idUsuario;

    public String getIdMovInv() {
        return this.idMovInv;
    }

    public void setIdMovInv(String idMovInv) {
        this.idMovInv = idMovInv;
    }

    public String getHora() {
        return this.hora;
    }

    public void setHora(String hora) {
        this.hora = hora;
    }

    public String getDescripcion() {
        return this.descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getMovimiento() {
        return this.movimiento;
    }

    public void setMovimiento(String movimiento) {
        this.movimiento = movimiento;
    }

    public String getHabia() {
        return this.habia;
    }

    public void setHabia(String habia) {
        this.habia = habia;
    }

    public String getCantidad() {
        return this.cantidad;
    }

    public void setCantidad(String cantidad) {
        this.cantidad = cantidad;
    }

    public String getHay() {
        return this.hay;
    }

    public void setHay(String hay) {
        this.hay = hay;
    }

    public String getCajero() {
        return this.cajero;
    }

    public void setCajero(String cajero) {
        this.cajero = cajero;
    }

    public String getDepartamento() {
        return this.departamento;
    }

    public void setDepartamento(String departamento) {
        this.departamento = departamento;
    }

    public String getIdUsuario() {
        return this.idUsuario;
    }

    public void setIdUsuario(String idUsuario) {
        this.idUsuario = idUsuario;
    }

}
